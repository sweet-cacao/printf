#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	size_t			i;

	i = 0;
	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (p1[i] && p2[i] && p1[i] == p2[i] && i < n)
		i++;
	if (i == n)
		i--;
	return (p1[i] - p2[i]);
}
